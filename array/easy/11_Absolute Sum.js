function getAbsSum(arr) {
	return arr=Math.abs()
}
console.log(getAbsSum([2, -1, -3, 4, 8])); //18);
console.log(getAbsSum([-1])); //1);
console.log(getAbsSum([-1, -3, -5, -4, -10, 0])); //23);
console.log(getAbsSum([8, 9, 10, 32, 101, -10])); //170);
console.log(getAbsSum([500])); //500);